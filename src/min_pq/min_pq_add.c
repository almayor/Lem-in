#include "min_pq.h"

void		min_pq_add(t_min_pq *pq, int i, int key)
{
	pq->size++;
	pq->qp[i] = pq->size;
	pq->pq[pq->size] = i;
	pq->keys[i] = key;
	min_pq_swim(pq, pq->size);
}
