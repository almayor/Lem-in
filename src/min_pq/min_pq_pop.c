#include "min_pq.h"

int 	min_pq_pop(t_min_pq *pq)
{
	int	min;

	min = pq->keys[pq->pq[1]];
	min_pq_swap(pq, 1, pq->size);
	min_pq_sink(pq, 1);
	pq->qp[pq->size] = 0;
	pq->size--;
	return (min);
}
