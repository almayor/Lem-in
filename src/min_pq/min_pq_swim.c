#include "min_pq.h"

void min_pq_swim(t_min_pq *pq, size_t k)
{
	while (k > 1 && pq->keys[k / 2] > pq->keys[k])
	{
		min_pq_swap(pq, k / 2, k);
		k = k / 2;
	}
}
	