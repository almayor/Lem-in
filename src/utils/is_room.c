#include "utils.h"

int	is_room(const char *s)
{
	if (!s)
		return (0);
	if (*s == 'L' || *s == '#')
		return (0);
	while (*s)
	{
		if (ft_isspace(*s))
			return (0);
		s++;
	}
	return (1);
}
